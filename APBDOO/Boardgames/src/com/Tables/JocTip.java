package com.Tables;

public class JocTip {
	private int joc_tip_id;
	private String nume;
	private String descriere;
	public int getJoc_tip_id() {
		return joc_tip_id;
	}
	public void setJoc_tip_id(int joc_tip_id) {
		this.joc_tip_id = joc_tip_id;
	}
	public String getNume() {
		return nume;
	}
	public void setNume(String nume) {
		this.nume = nume;
	}
	public String getDescriere() {
		return descriere;
	}
	public void setDescriere(String descriere) {
		this.descriere = descriere;
	}
}
